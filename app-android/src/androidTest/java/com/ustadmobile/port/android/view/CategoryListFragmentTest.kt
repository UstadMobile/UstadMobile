@AdbScreenRecord("Category screen tests")
class CategoryListFragmentTest : TestCase()  {

    @JvmField
    @Rule
    var dbRule = UmAppDatabaseAndroidClientRule(useDbAsRepo = true)

    @JvmField
    @Rule
    var systemImplNavRule = SystemImplTestNavHostRule()

    @JvmField
    @Rule
    val screenRecordRule = AdbScreenRecordRule()


    @AdbScreenRecord("Given list when Category clicked then navigate to CategoryDetail")
    @Test
    fun givenCategoryListPresent_whenClickOnCategory_thenShouldNavigateToCategoryDetail() {
        val testEntity = Category().apply {
            categoryName = "Test Name"
            categoryUid = dbRule.db.clazzDao.insert(this)
        }

        val fragmentScenario = launchFragmentInContainer(themeResId = R.style.UmTheme_App,
                fragmentArgs = bundleOf()) {
            CategoryListFragment().also {
                it.installNavController(systemImplNavRule.navController)
            }
        }

        init{

        }.run{

            CategoryListScreen{

                recycler{

                    childWith<CategoryListScreen.Category>{
                        withDescendant { withTag(testEntity.categoryUid) }
                    }perform {
                        title {
                            click()
                        }
                    }

                }

                flakySafely {
                    Assert.assertEquals("After clicking on item, it navigates to detail view",
                            R.id.category_detail_dest, systemImplNavRule.navController.currentDestination?.id)
                }


            }

        }
    }

}